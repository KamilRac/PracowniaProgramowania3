public class water
{
   private String name;
   private int mineralAmount;
   private boolean isCarbonated;
   
    public String getName(){
    return name;
    };
    
    public void setName(String newName){
    this.name=newName;
    };
    
    public int getMineralAmount(){
    return mineralAmount;
    };
    
    public void setMineralAmount(int newMineralAmount){
    this.mineralAmount=newMineralAmount;
    };
    
    
    public boolean getIsCarbonated(){
    return isCarbonated;
    };
    
    public void setIsCarbonated(boolean newIsCarbonated){
    this.isCarbonated=newIsCarbonated;
    }
    
}
